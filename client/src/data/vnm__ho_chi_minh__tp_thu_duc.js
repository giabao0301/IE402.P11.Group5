function convertToStyledPolygon(polygon) {
  const styledPolygon = {
    ward: polygon.properties.popupContent.split(", ")[0], // Name from the popupContent
    district: polygon.properties.popupContent.split(", ")[1], // Assuming a fixed location, can be modified
    province: "Thành Phố Hồ Chí Minh",
    symbol: {
      type: "simple-fill",
      color: [
        Math.floor(Math.random() * 256),
        Math.floor(Math.random() * 256),
        Math.floor(Math.random() * 256),
        0.5,
      ],
      outline: {
        color: [255, 255, 255],
        width: 2,
      },
    },
    rings: polygon.geometry.coordinates[0], // Coordinates from the provided polygon
  };
  return JSON.stringify(styledPolygon, null, 2);
}

